    private void jButtonProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProximoActionPerformed
        
        if (jButtonProximo.getText().equalsIgnoreCase("Próximo")){
            
            if (etapaCriacao == 1){
                
                String erro = editor.conjuntoHeuristico.validaNomeUnicoComponente(jTextFieldNomeEtapa.getText());
                if (erro != null){
                    UtilsGUI.dialogoErro(this,erro);
                    jTextFieldNomeEtapa.selectAll();
                    jTextFieldNomeEtapa.requestFocus();
                    return;
                }
            }
            
            etapaCriacao++;
            
            switch(etapaCriacao){
                case 2:card.show(jPanelPrincipal,"Passo2");
                    jButtonAnterior.setVisible(true);
                    break;
                    
                case 3:card.show(jPanelPrincipal,"Passo3");
                    jButtonProximo.setText("Concluir");
                    jButtonProximo.setMnemonic('n');
                    break;                        
            }
            
        }else{
            //////////////////////
            // Criar Nova Etapa //       
            //////////////////////
            
            String titulo    = jTextFieldTituloAnotacao.getText();
            String descricao = jTextAreaTextoAnotacao.getText();
            
            boolean faltaDadosAnotacao = false;
            
            if (titulo == null || titulo.trim().length() == 0){
                faltaDadosAnotacao = true;
                int resposta = UtilsGUI.dialogoConfirmacao(this,"O título da Anotação não foi preenchido!\n" +
                                                                "Deseja criar esta nova Etapa sem uma Anotação Inicial?",
                                                                "Aviso de Falta de Dados");
                
                if (resposta == JOptionPane.NO_OPTION || resposta == -1){
                    return;
                }
            }
            
            if (descricao == null || descricao.trim().length() == 0){
                faltaDadosAnotacao = true;
                int resposta = UtilsGUI.dialogoConfirmacao(this,"A descrição da Anotação Inicial não foi preenchido!\n" +
                                                                "Deseja criar esta Nova Etapa sem uma Anotação Inicial?",
                                                                "Aviso de Falta de Dados");
                
                if (resposta == JOptionPane.NO_OPTION || resposta == -1){
                    return;
                }
            }
            
            try{
                setCursor(new Cursor(Cursor.WAIT_CURSOR));
                
                Etapa etapa = new Etapa(editor.conjuntoHeuristico,
                                        UtilsString.preparaStringParaBD(jTextFieldNomeEtapa.getText(), true, Formato.TUDO_MAIUSCULO),
                                        HeuChess.usuario.getId(),
                                        EtapaDAO.tiposEtapas.get(0));
            
                etapa.setValorPeao(((Integer)  jSpinnerValorPeao.getValue()).intValue());
                etapa.setValorTorre(((Integer) jSpinnerValorTorre.getValue()).intValue());
                etapa.setValorCavalo(((Integer)jSpinnerValorCavalo.getValue()).intValue());
                etapa.setValorBispo(((Integer) jSpinnerValorBispo.getValue()).intValue());
                etapa.setValorDama(((Integer)  jSpinnerValorDama.getValue()).intValue());
                
                EtapaDAO.adiciona(editor.conjuntoHeuristico, etapa);
                
                if (!faltaDadosAnotacao){                           
                    Anotacao anotacao = new Anotacao(HeuChess.usuario.getId(),
                                                     etapa,
                                                    (Tipo) jComboBoxTipoAnotacao.getSelectedItem(),
                                                     titulo,
                                                     descricao);           
                
                    AnotacaoDAO.adiciona(anotacao);
                
                    etapa.getAnotacoes().add(anotacao);
                }
                
                ConexaoDBHeuChess.commit();
                        
                editor.incluiNovaEtapa(etapa);            
                
                dispose();   
                
                if (acessoTelaNovaEtapa instanceof TelaHeuristica){
                    ((TelaHeuristica) acessoTelaNovaEtapa).fechandoTelaNovaEtapa(etapa);                    
                }
                
            }catch(Exception e){
                HeuChess.desfazTransacao(e);
                
                setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
                UtilsGUI.dialogoErro(this, "Erro ao tentar criar a Etapa no Banco de Dados.\nOperação Cancelada!");
            }
        }
    }//GEN-LAST:event_jButtonProximoActionPerformed

