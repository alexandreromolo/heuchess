    private void habilitaProximoPasso1(){
        
        if (jTextFieldNomeEtapa.getText().trim().length() > 0){
            jButtonProximo.setEnabled(true);
        }else{
            jButtonProximo.setEnabled(false);
        }
    }
    
    public void cancelaCriacao(){
        
        int resposta = UtilsGUI.dialogoConfirmacao(this,"Deseja Cancelar a Criação desta Etapa?","Confirma Cancelamento");
        
        if (resposta == JOptionPane.YES_OPTION){      
            
            dispose();
            
            if (acessoTelaNovaEtapa instanceof TelaHeuristica){
                ((TelaHeuristica) acessoTelaNovaEtapa).fechandoTelaNovaEtapa(null);
            }
        }
    }
    
